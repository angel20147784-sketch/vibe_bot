WEEK_NAMES = {
    1: "Знакомство с вайбкодингом",
    2: "Инструменты и техники",
    3: "Первый реальный проект",
    4: "Продвинутые техники",
    5: "Финал и планы",
}

def format_day(day_num: int) -> str:
    day = COURSE_DAYS.get(day_num)
    if not day:
        return f"День {day_num} не найден. Доступны дни 1-30."
    
    week = (day_num - 1) // 7 + 1
    text = f"📚 ДЕНЬ {day_num}: {day['title']}\n"
    text += f"Неделя {week}: {WEEK_NAMES[week]}\n\n"
    text += f"{day['intro']}\n\n"
    
    for sec_title, items in day.get("sections", []):
        text += f"▸ {sec_title}:\n"
        for item in items:
            text += f"  • {item}\n"
        text += "\n"
    
    if day.get("steps"):
        text += "📋 Пошаговая инструкция:\n"
        for i, (title, desc) in enumerate(day["steps"], 1):
            text += f"{i}. {title} — {desc}\n"
        text += "\n"
    
    if day.get("tip"):
        text += f"💡 Совет: {day['tip']}\n"
    
    return text
