from fpdf import FPDF
import os

FONT_DIR = "C:/Windows/Fonts"

class CoursePDF(FPDF):
    def __init__(self):
        super().__init__()
        self.add_font("DV", "", os.path.join(FONT_DIR, "DejaVuSans.ttf"))
        self.add_font("DVB", "", os.path.join(FONT_DIR, "DejaVuSans-Bold.ttf"))
        self.add_font("DVM", "", os.path.join(FONT_DIR, "DejaVuSansMono.ttf"))
        self.set_auto_page_break(auto=False)
        self.page_count = 0

    def _bg(self, r, g, b):
        self.set_fill_color(r, g, b)
        self.rect(0, 0, 210, 297, "F")

    def _rect(self, x, y, w, h, r, g, b, border=0):
        self.set_fill_color(r, g, b)
        if border:
            self.set_draw_color(*border)
            self.set_line_width(0.5)
            self.rect(x, y, w, h, "DF")
        else:
            self.rect(x, y, w, h, "F")

    def _rrect(self, x, y, w, h, r, g, b):
        self.set_fill_color(r, g, b)
        self.rect(x, y, w, h, "F")

    def _txt(self, x, y, text, font="DV", size=11, r=45, g=54, b=54):
        self.set_font(font, "", size)
        self.set_text_color(r, g, b)
        self.set_xy(x, y)
        self.cell(0, 5, text, ln=0)

    def _txtc(self, x, y, text, font="DV", size=11, r=45, g=54, b=54):
        self.set_font(font, "", size)
        self.set_text_color(r, g, b)
        w = self.get_string_width(text)
        self.set_xy(x - w / 2, y)
        self.cell(w, 5, text, ln=0)

    def _txtr(self, x, y, text, font="DV", size=11, r=45, g=54, b=54):
        self.set_font(font, "", size)
        self.set_text_color(r, g, b)
        w = self.get_string_width(text)
        self.set_xy(x - w, y)
        self.cell(w, 5, text, ln=0)

    def _wrap(self, x, y, text, font="DV", size=10, r=45, g=54, b=54, max_w=170, lh=5.5):
        self.set_font(font, "", size)
        self.set_text_color(r, g, b)
        words = text.split()
        line = ""
        cy = y
        for word in words:
            test = f"{line} {word}" if line else word
            if self.get_string_width(test) < max_w:
                line = test
            else:
                if line:
                    self.set_xy(x, cy)
                    self.cell(0, lh, line, ln=0)
                    cy += lh
                line = word
        if line:
            self.set_xy(x, cy)
            self.cell(0, lh, line, ln=0)
            cy += lh
        return cy

    def _icon(self, x, y, letter, bg_r, bg_g, bg_b, sz=7):
        self.set_fill_color(bg_r, bg_g, bg_b)
        self.ellipse(x, y, sz, sz, "F")
        self.set_font("DVB", "", sz * 0.55)
        self.set_text_color(255, 255, 255)
        self.set_xy(x, y + sz * 0.12)
        self.cell(sz, sz * 0.7, letter, align="C", ln=0)

    def _step(self, y, num, title, desc, cr, cg, cb):
        self._rect(10, y, 190, 14, 255, 255, 255, border=(cr, cg, cb))
        self._rrect(10, y, 12, 14, cr, cg, cb)
        self.set_font("DVB", "", 9)
        self.set_text_color(255, 255, 255)
        self.set_xy(10, y + 3)
        self.cell(12, 8, str(num), align="C", ln=0)
        self._txt(25, y + 1, title, "DVB", 9, cr, cg, cb)
        self._wrap(25, y + 7, desc, "DV", 7.5, 100, 100, 100, 170)
        return y + 16

    def _draw_mockup_browser(self, y, title, elements):
        self._rect(10, y, 190, 55, 40, 44, 52)
        self._rect(10, y, 190, 8, 55, 60, 68)
        self.set_fill_color(255, 95, 86)
        self.ellipse(14, y + 2, 3.5, 3.5, "F")
